//Loops ou laços são estruturas de código que se repetem até que uma condição seja falsa.

//while 
let voltas = 0

//Enquanto o ciclista não completar as voltas ele tem que contnuar pedalando.
while (voltas < 10) {
console.log (`Pedale, você ja seu ${voltas} voltas.`)
//isso incrementa as voltas em 1.
//voltas ++ //voltas += 1 ou então voltas = voltas + 1 
voltas ++ 
}
console.log ("Você ganhou a corrida!")



//for significa para 
for (let voltas1 = 0; voltas1 < 10; voltas1 ++){
console.log (`Você ja deu ${voltas1} voltas.`)
}

console.log ("Parabéns, você ganhou a corrida!")